class ChannelAttention(nn.Module):
    def __init__(self, in_planes, ratio=16):
        super(ChannelAttention, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.max_pool = nn.AdaptiveMaxPool2d(1)

        self.fc1 = nn.Conv2d(in_planes, in_planes // ratio, 1, bias=False)
        self.relu1 = nn.ReLU()
        self.fc2 = nn.Conv2d(in_planes // ratio, in_planes, 1, bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        avg_out = self.fc2(self.relu1(self.fc1(self.avg_pool(x))))
        max_out = self.fc2(self.relu1(self.fc1(self.max_pool(x))))
        out = avg_out + max_out
        return self.sigmoid(out)


class SpatialAttention(nn.Module):
    def __init__(self, kernel_size=7):
        super(SpatialAttention, self).__init__()
        self.conv1 = nn.Conv2d(2, 1, kernel_size, padding=3,
                               bias=False)  # kernel size = 7 Padding is 3: (n - 7 + 1) + 2P = n
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        avg_out = torch.mean(x, dim=1, keepdim=True)
        max_out, _ = torch.max(x, dim=1, keepdim=True)
        x = torch.cat([avg_out, max_out], dim=1)
        x = self.conv1(x)
        return self.sigmoid(x)


class CBAM(nn.Module):
    def __init__(self, channelIn, channelOut):
        super(CBAM, self).__init__()
        self.channel_attention = ChannelAttention(channelIn)
        self.spatial_attention = SpatialAttention()

    def forward(self, x):
        out = self.channel_attention(x) * x
        # print('outchannels:{}'.format(out.shape))
        out = self.spatial_attention(out) * out
        return out


class HRFF(nn.Module):
    def __init__(self, c1, c2):
        super().__init__()
        C_in = int(c1 // 4)
        self.filter = Noise_filter(C_in, C_in)
        self.conv1 = C3(int(C_in * 2), C_in, shortcut=False)
        self.conv2 = C3(int(C_in * 2), C_in, shortcut=False)
        self.conv3 = C3(int(C_in * 2), C_in, shortcut=False)
        self.conv_yc = C3(int(C_in * 4), C_in, shortcut=False)
        self.conv_yl = C3(int(C_in * 4), C_in, shortcut=False)

    def forward_once(self, x):
        return self.filter(x)

    def forward(self, x):
        B, C, H, W = x.shape
        c_one = int(C // 4)
        x1 = x[:, 0:c_one, :, :]
        x2 = x[:, c_one:int(2 * c_one), :, :]
        x3 = x[:, int(2 * c_one):int(3 * c_one), :, :]
        x4 = x[:, int(3 * c_one):int(4 * c_one), :, :]

        x1 = self.forward_once(x1)
        x2 = self.forward_once(x2)
        x3 = self.forward_once(x3)
        x4 = self.forward_once(x4)

        x3 = self.conv3(torch.cat([x4, x3], dim=1))
        x2 = self.conv2(torch.cat([x3, x2], dim=1))
        x1 = self.conv1(torch.cat([x2, x1], dim=1))

        yc = self.conv_yc(torch.cat([x4, x3, x2, x1], dim=1))
        yl = self.conv_yl(torch.cat([x4, x3, x2, x1], dim=1))

        output = torch.cat([yc, yl], dim=1)

        return output


class Bottleneck_2(nn.Module):
    # Standard bottleneck
    def __init__(self, c1, c2, k=3, p=1, shortcut=True):  # ch_in, ch_out, shortcut, groups, expansion
        super().__init__()
        self.cv1 = Conv(c1, c1, 1, 1)
        self.cv2 = Conv(c1, c2, k, 1, p=p, g=1)
        self.add = shortcut and c1 == c2

    def forward(self, x):
        return x + self.cv2(self.cv1(x)) if self.add else self.cv2(self.cv1(x))


class Depth_wise_conv(nn.Module):
    # CSP Bottleneck with 3 convolutions
    def __init__(self, c1, c2, k=3, p=1, shortcut=True, e=0.5):  # ch_in, ch_out, number, shortcut, groups, expansion
        super().__init__()
        c_ = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, 1, 1)
        self.cv2 = Conv(c1, c_, 1, 1)
        self.cv3 = Conv(2 * c_, c2, 1)  # act=FReLU(c2)
        self.m = Bottleneck_2(c_, c_, k, p, shortcut)
        # self.m = nn.Sequential(*[CrossConv(c_, c_, 3, 1, g, 1.0, shortcut) for _ in range(n)])

    def forward(self, x):
        return self.cv3(torch.cat((self.m(self.cv1(x)), self.cv2(x)), dim=1))


class ChannelAttention_max(nn.Module):
    def __init__(self, in_planes, ratio=16):
        super().__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)

        self.fc1 = nn.Conv2d(in_planes, in_planes // ratio, 1, bias=False)
        self.relu1 = nn.ReLU()
        self.fc2 = nn.Conv2d(in_planes // ratio, in_planes, 1, bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        avg_out = self.fc2(self.relu1(self.fc1(self.avg_pool(x))))
        return self.sigmoid(avg_out) * x


class Noise_filter(nn.Module):
    def __init__(self, c1, c2):
        super().__init__()
        self.filter1 = Depth_wise_conv(c1, c1 // 2, k=7, p=3)
        self.filter2 = Depth_wise_conv(c1, c1 // 2, k=5, p=2)
        self.filter3 = Depth_wise_conv(c1, c1 // 2, k=3, p=1)
        # self.CA1 = ChannelAttention_max(c1 // 2)
        # self.CA2 = ChannelAttention_max(c1 // 2)
        # self.CA3 = ChannelAttention_max(c1)
        self.out_conv = Conv((c1 // 2) * 3, c2, k=1)

    def forward(self, x):
        # x1 = self.CA1(self.filter1(x))
        # x2 = self.CA2(self.filter2(x))
        # x3 = self.CA3(self.filter3(x))
        x1 = self.filter1(x)
        x2 = self.filter2(x)
        x3 = self.filter3(x)
        x = torch.cat([x1, x2, x3], dim=1)
        output = self.out_conv(x)
        return output



class SEModule(nn.Module):

    def __init__(self, c1, channels, num_layer):
        super(SEModule, self).__init__()
        self.num_layer = num_layer
        self.cv1 = Conv(channels, num_layer, 1)

    def forward(self, x):
        B, C, H, W = x.shape
        x_se = self.cv1(x)
        x = x.view(B, self.num_layer, C // self.num_layer, H, W)
        x_se = x_se.mean((2, 3), keepdim=True).view(B, self.num_layer, -1, 1, 1)
        x_se = x_se.sigmoid()
        x = (x * x_se).view(B, C, H, W)
        return x


class Conv(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, p=None, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.conv = nn.Conv2d(c1, c2, k, s, autopad(k, p), groups=g, bias=False)
        self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())

    def forward(self, x):
        return self.act(self.bn(self.conv(x)))

    def forward_fuse(self, x):
        return self.act(self.conv(x))


class DC(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, p=1, d=1, g=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.input = Conv(c1, c2, 1, 1, 0)
        self.conv = nn.Conv2d(c2, c2, k, s, p, d, groups=g, bias=False)
        self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())

    def forward(self, x):
        return self.act(self.bn(self.conv(self.input(x))))


class MRA(nn.Module):
    def __init__(self, c1, c2):
        super().__init__()
        in_c = c1 // 4
        self.input = nn.Conv2d(c1, in_c, 1, 1)
        self.dc1 = nn.Conv2d(in_c, in_c, 3, 1, 1, 1)
        self.dc2 = nn.Conv2d(in_c, in_c, 3, 1, 2, 2)
        self.dc3 = nn.Conv2d(in_c, in_c, 3, 1, 3, 3)
        self.dc4 = nn.Conv2d(in_c, in_c, 3, 1, 4, 4)
        self.output = nn.Conv2d(c1, c2, 3, 1, 1)

    def forward(self, x):
        x = self.input(x)
        x1 = self.dc1(x)
        x2 = self.dc2(x1)
        x3 = self.dc3(x2)
        x4 = self.dc4(x3)
        return self.output(torch.cat([x1, x2, x3, x4], 1))


class MRT(nn.Module):
    def __init__(self, c1, c2, k=3, s=1):
        super().__init__()
        self.dc1 = DC(c1, c2 // 4, k, s, p=1, d=1)
        self.dc2 = DC(c1, c2 // 4, k, s, p=2, d=2)
        self.dc3 = DC(c1, c2 // 4, k, s, p=3, d=3)
        self.dc4 = DC(c1, c2 // 4, k, s, p=4, d=4)
        self.conv = Conv(c2, c2)

    def forward(self, x):
        x1 = self.dc1(x)
        x2 = self.dc2(x)
        x3 = self.dc3(x)
        x4 = self.dc4(x)
        return self.conv(torch.cat([x1, x2, x3, x4], 1))



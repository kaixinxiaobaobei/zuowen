class BiFPN_Add2(nn.Module):
    def __init__(self, c1, c2, width_multiple=2.0):   # v5s width = 1.0  v5l width = 2.0
        super(BiFPN_Add2, self).__init__()
        self.w = nn.Parameter(torch.ones(2, dtype=torch.float32), requires_grad=True)
        self.epsilon = 0.0001
        # 根据深度和宽度倍数调整通道数
        self.conv = nn.Conv2d(int(c1 * width_multiple), int(c2 * width_multiple), kernel_size=1, stride=1, padding=0)
        self.silu = nn.SiLU()

    def forward(self, x):
        w = self.w
        weight = w / (torch.sum(w, dim=0) + self.epsilon)
        return self.conv(self.silu(weight[0] * x[0] + weight[1] * x[1]))


